<h2>Cari data dikolom pencarian . . . </h2>
<br>
   Seorang mukmin yang kuat lebih baik dan lebih Allah cintai daripada seorang mukmin yang lemah, dan masing-masing berada dalam kebaikan.
   Bersungguh-sungguhlah pada perkara-perkara yang bermanfaat bagimu, mintalah pertolongan kepada Allah dan janganlah kamu bersikap lemah.
   Jika kamu tertimpa sesuatu, janganlah kamu katakan: ‘Seandainya aku berbuat demikian, pastilah akan demikian dan demikian’
   Akan tetapi katakanlah: ‘Qoddarallah wa maa syaa fa’ala (Allah telah mentakdirkan hal ini dan apa yang dikehendakiNya pasti terjadi)’.
   Sesungguhnya perkataan ‘Seandainya’ membuka pintu perbuatan setan

  <span>Rasulullah Shallallahu Alaihi Wassalam</span>
